#include "../includes/libftprintf.h"

t_structpf	ft_put_0(t_structpf pf)
{
	while (pf.nb2 != 0)
	{
		pf.str[pf.tempi] = '0';
		pf.tempi++;
		pf.nb2--;
	}
	return (pf);
}
